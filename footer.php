<script>
// Open popup
function openDiscoveryPopup() {
    document.getElementById('discoveryPopup').classList.add('active');
    document.body.style.overflow = 'hidden'; // Prevent background scroll
}

// Close popup
function closeDiscoveryPopup() {
    document.getElementById('discoveryPopup').classList.remove('active');
    document.body.style.overflow = ''; // Restore scroll
}

// Close on Escape key
document.addEventListener('keydown', function(event) {
    if (event.key === 'Escape') {
        closeDiscoveryPopup();
    }
});

// Add animation class when popup opens
document.getElementById('discoveryPopup').addEventListener('transitionend', function() {
    if (this.classList.contains('active')) {
        this.querySelector('.discovery-popup-content').classList.add('show');
    } else {
        this.querySelector('.discovery-popup-content').classList.remove('show');
    }
});
</script>

